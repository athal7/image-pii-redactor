/**
 * End-to-end tests for <pii-redactor>.
 *
 * These tests run in a real browser via Playwright and exercise the full
 * pipeline: image upload → OCR → NER → SVG overlay → export.
 *
 * IMPORTANT: The first run downloads the NER model (~80MB). Subsequent runs
 * use the browser cache. Set the SKIP_MODEL_DOWNLOAD env var to skip tests
 * that require model inference (for fast CI pre-flight checks).
 *
 * Performance design
 * ──────────────────
 * OCR (Tesseract WASM init + recognition) and NER (Transformers.js inference)
 * are the expensive steps — each takes several seconds even with a warm cache.
 * To avoid re-running them for every assertion, each pipeline describe block
 * shares a single browser page via beforeAll: the image is uploaded once, the
 * pipeline runs to "reviewing", and all read-only assertions in that block
 * inspect the already-resolved state.
 *
 * Tests that mutate component state (confirm, cancel, toggle) need a clean
 * component, so they open their own page and upload independently.
 */

