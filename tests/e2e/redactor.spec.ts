// ── Full pipeline — shared page ───────────────────────────────────────────────
//
// One upload drives all read-only assertions in this block.
// Mutating tests (confirm, cancel, toggle) manage their own page.

test.describe("pipeline tests (requires model download)", () => {
  test.setTimeout(300_000);

  // Shared reviewing-phase page — upload runs once in beforeAll.
  let sharedPage: Page;

  test.beforeAll(async ({ browser }) => {
    sharedPage = await openReviewingPage(browser, TEST_IMAGE);
  });

  test.afterAll(async () => {
    await sharedPage.close();
  });

  test("uploading a screenshot starts the loading phase", async ({ page }) => {
    // Only needs to observe the phase transition away from idle — not pipeline
    // output. Use a 1×1 minimal image so OCR finishes near-instantly and the
    // full pipeline doesn't run unnecessarily in the background.
    const [fileChooser] = await Promise.all([
      page.waitForEvent("filechooser"),
      page.evaluate(() => {
        document.querySelector("pii-redactor")?.shadowRoot
          ?.querySelector<HTMLElement>(".dropzone")?.click();
      }),
    ]);
    await fileChooser.setFiles(FIXTURES.minimal);

    await page.waitForFunction(() => {
      const el = document.querySelector("pii-redactor") as any;
      return el?.phase !== "idle";
    }, { timeout: 10_000 });

    const phase = await shadowProp(page, "phase");
    expect(["loading", "ocr", "detecting", "reviewing"]).toContain(phase);
  });

  test("pipeline reaches reviewing phase and renders SVG redaction boxes", async () => {
    const rectCount = await countRedactionBoxes(sharedPage);
    expect(rectCount).toBeGreaterThan(0);
  });

  test("entity list shows detected items", async () => {
    const headerText = await sharedPage.evaluate(() => {
      const host = document.querySelector("pii-redactor");
      if (!host?.shadowRoot) return "";
      const el = host.shadowRoot.querySelector(".entity-list-header") as HTMLElement | null;
      return el?.innerText?.replace(/\s+/g, " ").trim() ?? "";
    });
    expect(headerText).toMatch(/Detected items \(\d+ \/ \d+\)/);
    const [enabled, total] = headerText.match(/\d+/g)!.map(Number);
    expect(total).toBeGreaterThan(0);
    expect(enabled).toBe(total);
  });

  test("user can toggle a redaction off via the entity list", async ({ browser }) => {
    // Mutates component state — needs its own fresh reviewing page.
    const page = await openReviewingPage(browser, TEST_IMAGE);
    try {
      await page.evaluate(() => {
        const host = document.querySelector("pii-redactor");
        host?.shadowRoot
          ?.querySelector<HTMLInputElement>(".entity-item input[type=checkbox]")
          ?.click();
      });
      await page.waitForTimeout(300);

      const headerText = await shadowText(page, ".entity-list-header");
      const [enabled, total] = headerText.match(/\d+/g)!.map(Number);
      expect(enabled).toBe(total - 1);
    } finally {
      await page.close();
    }
  });

  test("confirm & redact produces a redaction-complete event with a PNG blob", async ({
    browser,
  }) => {
    // Mutates component state (transitions to done) — needs its own page.
    const page = await openReviewingPage(browser, TEST_IMAGE);
    try {
      let eventFired = false;
      let blobSize = 0;

      await page.exposeFunction("onRedactionComplete", (size: number) => {
        eventFired = true;
        blobSize = size;
      });
      await page.evaluate(() => {
        document.querySelector("pii-redactor")?.addEventListener(
          "redaction-complete",
          (e: any) => (window as any).onRedactionComplete(e.detail.blob.size),
        );
      });

      await page.evaluate(() => {
        const host = document.querySelector("pii-redactor");
        host?.shadowRoot?.querySelector<HTMLElement>("button.primary")?.click();
      });

      await page.waitForFunction(() => (window as any).__redactionDone, {
        timeout: 30_000,
      });
      expect(eventFired).toBe(true);
      expect(blobSize).toBeGreaterThan(1000);
    } finally {
      await page.close();
    }
  });

  test("cancel button fires redaction-cancel and resets to idle", async ({ browser }) => {
    // Mutates component state (transitions to idle) — needs its own page.
    const page = await openReviewingPage(browser, TEST_IMAGE);
    try {
      let cancelFired = false;
      await page.exposeFunction("onRedactionCancel", () => { cancelFired = true; });
      await page.evaluate(() => {
        document.querySelector("pii-redactor")?.addEventListener(
          "redaction-cancel",
          () => (window as any).onRedactionCancel(),
        );
      });

      await page.evaluate(() => {
        const host = document.querySelector("pii-redactor");
        host?.shadowRoot?.querySelector<HTMLElement>("button.danger")?.click();
      });

      await page.waitForFunction(
        () => { const el = document.querySelector("pii-redactor") as any; return el?.phase === "idle"; },
        { timeout: 5_000 },
      );

      expect(cancelFired).toBe(true);
      const phase = await shadowProp(page, "phase");
      expect(phase).toBe("idle");
    } finally {
      await page.close();
    }
  });
});

