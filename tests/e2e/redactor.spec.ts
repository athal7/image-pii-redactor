// ── Helpers ───────────────────────────────────────────────────────────────────

// Get a property directly off the <pii-redactor> element
async function shadowProp(page: Page, selector: string): Promise<unknown> {
  return page.evaluate((sel) => {
    const el = document.querySelector("pii-redactor") as any;
    return el?.[sel];
  }, selector);
}

// Wait for the component to reach a given phase
async function waitForPhase(page: Page, phase: string, timeout = 240_000) {
  await page.waitForFunction(
    (p) => {
      const el = document.querySelector("pii-redactor") as any;
      return el?.phase === p;
    },
    phase,
    { timeout },
  );
}

// Wait for idle phase (used in beforeAll/beforeEach helpers)
async function waitForIdle(page: Page) {
  await page.waitForSelector("pii-redactor");
  await page.waitForFunction(() => {
    const el = document.querySelector("pii-redactor") as any;
    return el?.phase === "idle";
  });
}

// Get the text content of a shadow DOM element
async function shadowText(page: Page, cssSelector: string): Promise<string> {
  return page.evaluate((sel) => {
    const host = document.querySelector("pii-redactor");
    if (!host?.shadowRoot) return "";
    return host.shadowRoot.querySelector(sel)?.textContent?.trim() ?? "";
  }, cssSelector);
}

// Upload a file via the dropzone and wait for the reviewing phase
async function uploadAndReview(page: Page, imagePath: string) {
  const [fileChooser] = await Promise.all([
    page.waitForEvent("filechooser"),
    page.evaluate(() => {
      document
        .querySelector("pii-redactor")
        ?.shadowRoot?.querySelector<HTMLElement>(".dropzone")
        ?.click();
    }),
  ]);
  await fileChooser.setFiles(imagePath);
  await waitForPhase(page, "reviewing");
}

// Count redaction boxes in the SVG overlay
async function countRedactionBoxes(page: Page): Promise<number> {
  return page.evaluate(() => {
    const host = document.querySelector("pii-redactor");
    const svgs = host?.shadowRoot?.querySelectorAll("svg");
    if (!svgs) return 0;
    for (const svg of svgs) {
      const rects = svg.querySelectorAll("rect.redaction-box");
      if (rects.length > 0) return rects.length;
    }
    return 0;
  });
}

// Open a fresh page, navigate to the demo, and upload an image to reviewing.
// Returns the page — caller is responsible for closing it.
async function openReviewingPage(browser: Browser, imagePath: string): Promise<Page> {
  const page = await browser.newPage();
  await page.goto(DEMO_URL);
  await waitForIdle(page);
  await uploadAndReview(page, imagePath);
  return page;
}

